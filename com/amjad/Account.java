package com.amjad;

public class Account {
  private int AccNum;
  private String name;
  
  
  public void setAccNum(int AccNum) {
	  this.AccNum = AccNum;
  }
   public int getAccNum() {
	    return this.AccNum;
   }
	  
   public void setName(String name) {
	   this.name = name ;
	  }
   
   public String getName() {
	   return this.name;
   }
}
